// java program to clear in doubt in usage of d in double variables

class Main{

	public static void main(String...a){	
		double var1=112.0000000; //implicit conversion 
		double var2=122.0000000d; //d is used to explicitly mention as double value	
		System.out.println("Printing var before appending d "+var1);
		System.out.println("Printing var after appending d "+var2);
	}
}
		